package org.uniworks.groupware.service.internal; 

import java.util.List; 
import java.util.Map; 

import org.springframework.beans.factory.annotation.Autowired; 
import org.springframework.stereotype.Service; 
import org.springframework.transaction.annotation.Propagation; 
import org.springframework.transaction.annotation.Transactional; 

import org.uniworks.groupware.domain.Nw131m; 
import org.uniworks.groupware.mapper.Nw131mMapper; 
import org.uniworks.groupware.service.Nw131mService; 

@Service 
@Transactional(readOnly = true) 
public class Nw131mServiceImpl implements Nw131mService { 
	@Autowired Nw131mMapper nw131mMapper; 

	/** 
	 * 목록을 조회한다. 
	 * 
	 * @return 
	 */ 
	@Override 
	public List<Nw131m> getNw131mList(Map<String, Object> map) { 
		return nw131mMapper.select(map); 
	} 
	/** 
	 * 조회한다. 
	 * 
	 * @return 
	 */ 
	@Override 
	public Nw131m getNw131m(Map<String, Object> map) { 
		return nw131mMapper.selectByPrimaryKey(map); 
	} 
	/** 
	 * 등록한다. 
	 * 
	 * @return 
	 */ 
	@Override 
	@Transactional(readOnly = false, propagation = Propagation.REQUIRED) 
	public int addNw131m(Nw131m nw131m) { 
		return nw131mMapper.insert(nw131m); 
	} 
	/** 
	 * 수정한다. 
	 * 
	 * @return 
	 */ 
	@Override 
	@Transactional(readOnly = false, propagation = Propagation.REQUIRED) 
	public int updateNw131m(Nw131m nw131m) { 
		return nw131mMapper.updateByPrimaryKey(nw131m); 
	} 
	/** 
	 * 삭제한다. 
	 * 
	 * @return 
	 */ 
	@Override 
	@Transactional(readOnly = false, propagation = Propagation.REQUIRED) 
	public int deleteNw131m(Map<String, Object> map) { 
		return nw131mMapper.deleteByPrimaryKey(map); 
	} 
} 
